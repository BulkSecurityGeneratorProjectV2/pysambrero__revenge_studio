function remoteInstruction(data, timeout, beforeSendCallback, successCallback, errorCallback, completeCallback) {
    $.ajax({
        type: 'POST',
        url: '/api/protected/remoteinstruction',
        data: data,
        dataType: 'text',
        async: true,
        timeout: timeout, // sets timeout to 3 seconds

        beforeSend: function () {
            if (beforeSendCallback)
                beforeSendCallback();
        },
        success: function (data, status, xhr) {
            if (successCallback)
                successCallback(data);

        },
        error: function (xhr) {
            var msg;
            if (xhr.responseText === "undefined" || !xhr.responseText) {
                msg = xhr.statusText;
            } else {
                msg = xhr.statusText + ": " + xhr.responseText;
            }
            notify(msg, "error", 5000);
            if (errorCallback) {
                errorCallback(msg);
            }
        },
        complete: function () {
            if (completeCallback)
                completeCallback();
        }
    });
}

